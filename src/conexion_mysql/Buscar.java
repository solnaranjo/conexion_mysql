    public void Buscar(String valor, String filtro, JTable tablaUsuarios){
    
        String [] columnas = {"ID", "Nombre", "Apellido", "usuario", "contrase√±a", "fecha"};
        String [] registro = new String[6];
        ModeloTabla  = new DefaultTableModel(null,columnas);      
        String SSQL;
       
        
        if(filtro.equals("apellidoUsuario")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE apellidoUsuario LIKE '%"+valor+"%'";
        
        }else if(filtro.equals("nombreUsuario")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE nombreUsuario LIKE '%"+valor+"%'";
    
        }else if(filtro.equals("usuario")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE usuario LIKE '%"+valor+"%'";
    
        }else if(filtro.equals("contrasena")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE contrasena LIKE '%"+valor+"%'";
    
        } else if(filtro.equals("idDocumento")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE idDocumento LIKE '%"+valor+"%'";
                
                System.out.println(SSQL);
    
        }else if (filtro.equals("fecha")){
    
                SSQL= "SELECT idDocumento, nombreUsuario, apellidoUsuario, usuario, contrasena, fecha "
                 + "FROM usuarios WHERE fecha LIKE '%"+valor+"%'";
                
                System.out.println(SSQL);
                
        } else{
        
                SSQL= "SELECT * FROM usuarios;";
        
        
        }// Fin if
        
        //
        try (Statement st = conn.createStatement()) {
                
                // ejecuta la consulta y obtiene el resultado
                ResultSet rs = st.executeQuery(SSQL);
                
                // recorre la respuesta
                while (rs.next()) {
                 
                    registro[0] = rs.getString("idDocumento");
                    registro[1] = rs.getString("nombreUsuario");
                    registro[2] = rs.getString("apellidoUsuario");
                    registro[3] = rs.getString("usuario");
                    registro[4] = rs.getString("contrasena");
                    registro[5] = rs.getString("fecha");
                 
                    
                    ModeloTabla.addRow(registro);
                    
                }//fin while
                
                // llena la tabla de la vista
                tablaUsuarios.setModel(ModeloTabla);
                
            } catch (SQLException ex) {
                
                //en caso de error, imprime el tipo de error
                System.err.println(ex.getMessage());
            
            }//fin trycatch
    
        
    }// Fin del metodo Buscar
    
